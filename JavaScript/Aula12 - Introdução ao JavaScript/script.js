// Variaveis - VAR e LET

//Declarando VAR
var texto = "Hello World"
console.log(texto)

//Declarando LET
let nome = "Vitor Hideki"
console.log("Nome: ", nome)

//Atribuindo um novo valor - LET
nome = "Jonas"
console.log("Nome: ", nome)

//Atribuindo um novo valor - VAR
texto = "Olá, Mundo"
console.log(texto)

//Constantes - const
const valorPI = 3.14
console.log("O valor de PI é igual a",valorPI)

